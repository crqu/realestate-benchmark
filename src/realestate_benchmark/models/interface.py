"""Model interface protocol for LLM implementations.

This module defines the abstract interface that all LLM model implementations
must satisfy, along with supporting dataclasses for messages, tools, and responses.

Example:
    Implementing a custom model:

    >>> from realestate_benchmark.models.interface import ModelInterface, Message, ModelResponse
    >>>
    >>> class CustomModel:
    ...     def __init__(self, api_key: str):
    ...         self._model_name = "custom-model-v1"
    ...
    ...     def generate(
    ...         self,
    ...         messages: list[Message],
    ...         tools: list[ToolDefinition] | None = None,
    ...         temperature: float = 0.7,
    ...     ) -> ModelResponse:
    ...         # Implementation here
    ...         return ModelResponse(
    ...             content="Generated response",
    ...             tool_calls=[],
    ...             usage={"input_tokens": 100, "output_tokens": 50},
    ...             model_name=self._model_name,
    ...         )
    ...
    ...     @property
    ...     def model_name(self) -> str:
    ...         return self._model_name
    ...
    ...     @property
    ...     def supports_tool_use(self) -> bool:
    ...         return True
"""

from typing import Any, Protocol

from pydantic import BaseModel, Field


class Message(BaseModel):
    """A message in a conversation.

    Attributes:
        role: The role of the message sender. Must be one of:
            - "system": System-level instructions
            - "user": User or human input
            - "assistant": Assistant or model output
        content: The text content of the message.
    """

    role: str = Field(..., description='Message role: "system", "user", or "assistant"')
    content: str = Field(..., description="Text content of the message")


class ToolDefinition(BaseModel):
    """Definition of a tool/function that the model can call.

    Attributes:
        name: The unique identifier for the tool.
        description: Human-readable description of what the tool does.
        parameters: JSON schema defining the tool's parameters.
            Should follow JSON Schema Draft 2020-12 format.
    """

    name: str = Field(..., description="Unique tool identifier")
    description: str = Field(..., description="Description of tool functionality")
    parameters: dict[str, Any] = Field(
        ..., description="JSON schema for tool parameters (JSON Schema Draft 2020-12)"
    )


class ToolCall(BaseModel):
    """A request from the model to call a specific tool.

    Attributes:
        name: The name of the tool to call, matching a ToolDefinition name.
        arguments: Dictionary of parameter names to values for the tool call.
    """

    name: str = Field(..., description="Name of the tool to call")
    arguments: dict[str, Any] = Field(..., description="Parameter values for the tool call")


class ModelResponse(BaseModel):
    """Response from a model generation request.

    Attributes:
        content: The text content generated by the model.
        tool_calls: List of tool calls requested by the model. Empty if the model
            generated only text.
        usage: Token usage statistics with keys:
            - "input_tokens": Number of tokens in the input
            - "output_tokens": Number of tokens generated
        model_name: Identifier of the specific model that generated this response.
    """

    content: str = Field(..., description="Generated text content")
    tool_calls: list[ToolCall] = Field(
        default_factory=list, description="Tools requested by the model"
    )
    usage: dict[str, int] = Field(
        ..., description='Token usage: {"input_tokens": int, "output_tokens": int}'
    )
    model_name: str = Field(..., description="Model identifier that generated this response")


class ModelInterface(Protocol):
    """Protocol defining the interface for LLM model implementations.

    All model implementations (Anthropic, Vertex AI, OpenAI, Mock, etc.)
    must satisfy this protocol to be used by agents in the benchmark.

    The protocol defines the core generation method and two required properties.
    """

    def generate(
        self,
        messages: list[Message],
        tools: list[ToolDefinition] | None = None,
        temperature: float = 0.7,
    ) -> ModelResponse:
        """Generate a response from the model.

        Args:
            messages: Conversation history, including system prompts, user messages,
                and prior assistant responses. The last message is typically from
                the user role.
            tools: Optional list of tools the model can choose to call. If None,
                the model will only generate text. If provided, the model may
                return tool calls in the response.
            temperature: Sampling temperature controlling randomness. Higher values
                (e.g., 1.0) produce more creative outputs; lower values (e.g., 0.0)
                produce more deterministic outputs. Default is 0.7.

        Returns:
            ModelResponse containing the generated content, any tool calls,
            token usage statistics, and the model identifier.

        Raises:
            Exception: Implementation-specific exceptions for API errors,
                invalid parameters, or other failures.
        """
        ...

    @property
    def model_name(self) -> str:
        """Return the model identifier.

        Returns:
            String identifier for the specific model (e.g., "claude-sonnet-4-5",
            "gpt-4", "gemini-pro").
        """
        ...

    @property
    def supports_tool_use(self) -> bool:
        """Return whether this model supports tool calling.

        Returns:
            True if the model can process tool definitions and return tool calls,
            False if it only supports text generation.
        """
        ...
